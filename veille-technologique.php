           <div class="resume-item">
              <h4>Description</h4>
              <!--<h5>2019 - Present</h5>-->
              <p><em>Un framework est un ensemble d'outils et de composants logiciels organisés conformément à un plan d'architecture et des patterns, l'ensemble formant ou promouvant un « squelette » de programme, un canevas. Il est souvent fourni sous la forme d'une bibliothèque logicielle et accompagné du plan de l'architecture cible du framework5.</em></p>
            </div>

            <div class="resume-item">
              <h4>Importance</h4>
         
              <p><em>Un framework est conçu en vue d'aider les programmeurs dans leur travail. L'organisation du framework vise la productivité maximale du programmeur qui va l'utiliser — gage de baisse des coûts de construction et maintenance du programme. Le contenu exact du framework est dicté par le type de programme et l'architecture cible pour lequel il est conçu.</em></p>
              <p>
              <ul>
                <li>framework d'infrastructure système : pour développer des systèmes d'exploitation, des interfaces graphiques, des outils de communication (exemple : Framework .Net, Struts);</li>
                <li>framework d'intégration intergicielle (middleware) : pour fédérer des applications hétérogènes. Pour mettre à disposition différentes technologies sous la forme d'une interface unique;</li>
                <li>frameworks d'entreprise : pour développer des applications spécifiques au secteur d'activité de l'entreprise;</li>
                <li>frameworks de gestion de contenu : sont les fondations d'un système de gestion de contenu6 — pour la création, la collecte, le classement, le stockage et la publication de « biens numérisés ».</li>
              </ul>
             </p>
            </div>
           
            <div class="resume-item">
              <h4>Les avantages d'un framework</h4>
             
              <p><em>
                - Réduction des erreurs : Les frameworks fournissent souvent des conventions de codage et des règles pour les développeurs, ce qui permet de réduire les erreurs et les bugs dans le code.<br><br>
                - Évolutivité : Les frameworks sont conçus pour être évolutifs, ce qui signifie qu'ils peuvent s'adapter à des projets de toutes tailles, du plus petit site web au plus grand système d'entreprise.<br><br>
                - Sécurité : Les frameworks peuvent fournir des fonctionnalités de sécurité intégrées pour protéger les applications contre les attaques courantes telles que les injections SQL, les attaques de type XSS, etc.<br><br>
                - Meilleure collaboration : Les frameworks fournissent souvent une structure et des conventions claires pour le développement, ce qui permet aux développeurs de mieux collaborer et de travailler ensemble sur un même projet.<br><br>
                - Réutilisabilité : Les frameworks fournissent souvent des bibliothèques de code réutilisable, ce qui permet de réduire le temps et le coût de développement en évitant de réécrire du code existant.
              </em></p>
